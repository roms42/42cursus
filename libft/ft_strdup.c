#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	slen;
	char	*dst;
	size_t	i;

	i = 0;
	slen = ft_strlen(s1);
	dst = malloc(sizeof(char) * (slen + 1));
	if (!dst)
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = 0;
	return (dst);
}
