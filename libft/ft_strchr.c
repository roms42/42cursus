#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		s = NULL;
	while (*s && *s != (char)c)
		s++;
	if (*s == 0 && (char)c != 0)
		s = NULL;
	return ((char*)(s));
}
